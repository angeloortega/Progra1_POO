    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String correo = jTextField5.getText();
        String nombre = jTextField6.getText();
        EmailValidator validator = new EmailValidator();
            if(jTextArea2.getText().contains(correo)){
                JOptionPane.showMessageDialog(this, "Ya se ha añadido este "
                        + "correo de participantes");
                return;
            }
            if(validator.validate(correo)){
                if(nombre.contains(" ") && nombre.length() > 2){
                    if(contador<capacidad_requerida){
                    participantes.add(new Participante(nombre, correo));
                    contador += 1;
                    if(jTextArea2.getText().length() > 0){
                        jTextArea2.setText(jTextArea2.getText() +"\n" + "Nombre: "
                        + nombre + " | Correo: " + correo);
                        }
                    else{
                        jTextArea2.setText("Nombre: "
                        + nombre + " | Correo: " + correo);
                        }
                    jTextField5.setText("");
                    jTextField6.setText("");
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Ha alcanzado "
                                + "el máximo de integrantes específicado");
                    }
                }else{
                
                JOptionPane.showMessageDialog(this, "Inserte por lo "
                                 + "menos un nombre y un apellido");
                }
            }
            else{
                JOptionPane.showMessageDialog(this, "El correo electrónico "
                    +correo+ " es invalido");        
            }
    }//GEN-LAST:event_jButton3ActionPerformed

