    /**
     * Method that occurs when the "Search for rooms" button is pressed
     * it enables the rest of the interface in which the user writes the
     * information relevant to the reserve
     * @param evt 
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jComboBox1.removeAllItems();
        jComboBox1.setEnabled(false);
        jTextField3.setEnabled(false);
        jTextField5.setEnabled(false);
        jTextField6.setEnabled(false);
        jButton3.setEnabled(false);
        jButton4.setEnabled(false);
        jTextArea2.setEnabled(true);
        jTextField3.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextArea2.setText("");
        participantes = new ArrayList<>();
        contador = 1;
        String recursos_seleccionados = jTextArea1.getText();
        ArrayList<String> recursos;
        dateAndTime = dateTimePicker2.getDateTimeStrict();
        time = timePicker1.getTime();
        String capacidad = jTextField1.getText();
        if(!(capacidad.chars().allMatch(Character::isDigit) && capacidad.length()>0 && capacidad.length()<3 && capacidad.charAt(0) != '0')){
            JOptionPane.showMessageDialog(this, "La capacidad debe ser un entero entre 3 y 99");
            return;
        }
        if((int) Integer.parseInt(capacidad) < 3){
            JOptionPane.showMessageDialog(this, "La capacidad minima de participantes es de 3");
            return;
        }
        else{
            capacidad_requerida = (int) Integer.parseInt(capacidad);
        }
        if(!"".equals(recursos_seleccionados)){
            recursos = new ArrayList<>(Arrays.asList(recursos_seleccionados.split("\n")));
        }
        else{
            recursos = new ArrayList<>();
        }
        if(dateAndTime.toLocalTime().isAfter(time) || dateAndTime.toLocalTime().equals(time)){
            JOptionPane.showMessageDialog(this, "La hora de inicio tiene que "
                    + "ser menor a la hora final");
            return;
        }
        if((dateAndTime.toLocalTime().isBefore(LocalTime.now()) || dateAndTime.toLocalTime().equals(LocalTime.now())) && dateAndTime.equals(LocalDateTime.now())){
            JOptionPane.showMessageDialog(this, "La reserva debe hacerse con"
                    + " 30 minutos de antelación");
            return;
        }
        LocalDateTime ahora = LocalDateTime.now();
        LocalDateTime inAWeek = ahora.plusWeeks(1);
        if(dateAndTime.isAfter(inAWeek)){
            JOptionPane.showMessageDialog(this, "Solo se pueden hacer reservas"
                    + " en un rango de una semana");
            return;
        }
        if(dateAndTime.isBefore(ahora)){
            JOptionPane.showMessageDialog(this, "No se pueden hacer reservas"
                    + " en el pasado");
            return;
        }
        ArrayList<Sala> salas_disponibles = AplSystem.BuscarSalas(dateAndTime, time, recursos, capacidad_requerida);
        if(salas_disponibles.isEmpty()){
            JOptionPane.showMessageDialog(this, "No hay salas disponibles"
                    + " que cumplan con sus requisitos por favor"
                    + " modifique los requisitos e intente nuevamente.");
        }
        else{
            for(Sala sala: salas_disponibles){
                jComboBox1.addItem(sala.getId());
            }
            jComboBox1.setEnabled(true);
            jTextField3.setEnabled(true);
            jTextField5.setEnabled(true);
            jTextField6.setEnabled(true);
            jButton3.setEnabled(true);
            jButton4.setEnabled(true);
            jTextArea2.setEnabled(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
