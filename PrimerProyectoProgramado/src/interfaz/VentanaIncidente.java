    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jComboBox2.getSelectedIndex() > -1){
            Estudiante eTemp = AplSystem.getEstudiante(jTextField1.getText());
            eTemp.agregarIncidente(jComboBox2.getSelectedIndex());
            jComboBox1.removeAllItems();
            jComboBox1.setEnabled(false);
            jComboBox2.setEnabled(false);
            jButton2.setEnabled(false);
            jTextField1.setEnabled(true);
            jTextField1.setText("");
            String emisor="edjiron@gmail.com";
            String contraseña="jqalrtwrjxnkhpic";
                String asunto="Incidencia registrada en el sistema de manejos de aulas";
                
                String texto="Se ha registrado una incidencia a su usario del sistema"
                        + "de manejo de aulas:\n";
                texto += eTemp.getUltimoIncidente().toString();
                ArrayList<String> receptor = new ArrayList<>();
                receptor.add(eTemp.getCorreo());
                Correo correo=new Correo(emisor,contraseña,receptor,asunto,texto);
                try {
                    correo.sendCorreo();
                } catch (MessagingException ex) {
                    Logger.getLogger(VentanaCrearReserva.class.getName()).log(Level.SEVERE, null, ex);
                }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

