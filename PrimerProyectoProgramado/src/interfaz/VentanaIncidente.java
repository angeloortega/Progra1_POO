    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String carnet = jTextField1.getText();
        if(carnet.chars().allMatch( Character::isDigit) && carnet.length() == 10){
            if(AplSystem.getListaCarnets().contains(carnet)){
                for(Reserva r: AplSystem.getReservas()){
                    if(r.getEstudiante().getCarne().equals(carnet)){
                        //if(r.getFin().isAfter(LocalDateTime.now())){
                            //JOptionPane.showMessageDialog(this, "La calificación"
                                //+ " debe hacerse despues de que la reserva"
                                //+ " culmine");
                            //return;
                            //}
                   
                    jComboBox1.addItem(r.getCodigo());
                    jComboBox1.setEnabled(true);
                    jComboBox2.setEnabled(true);
                    jButton2.setEnabled(true);
                    jTextField1.setEnabled(false);
                    }
                }
                
            }
            else{
                JOptionPane.showMessageDialog(this, "El carnet " + carnet+ " no "
                + "esta registrado en el sistema");
                        }
        }
        else{
            JOptionPane.showMessageDialog(this, "El carnet debe ser un número");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

