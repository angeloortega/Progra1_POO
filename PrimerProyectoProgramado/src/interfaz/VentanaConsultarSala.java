    /**
     * An identification number is searched, if found, the room information
     * is displayed
     * @param evt 
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ArrayList<String> salas = AplSystem.getListaIDs();
        String id = jTextField1.getText().toUpperCase();
        if(salas.contains(id)){
            Sala e = AplSystem.getSala(id);
            jTextArea1.setText(null);
            jTextArea1.setText(e.toString());
        }
        else{
            JOptionPane.showMessageDialog(this, "La sala " + 
                    jTextField1.getText() +" no está registrada en el sistema "+
                    "(El formato correcto de sala es SAL-000)");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

