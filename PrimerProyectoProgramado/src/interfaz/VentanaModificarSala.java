    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jComboBox2.getSelectedItem() != null){
            if(jTextArea1.getText().length() > 0){
                jTextArea1.setText(jTextArea1.getText() +"\n" + (String) jComboBox2.getSelectedItem());
            }
            else{
                jTextArea1.setText((String) jComboBox2.getSelectedItem());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    /**
     * Determines what information has been modified and stores the changes
     * @param evt 
     */
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String recursos_seleccionados = jTextArea1.getText();
        ArrayList<String> recursos = new ArrayList<>(Arrays.asList(recursos_seleccionados.split("\n")));
        if(jTextArea1.getText().length() > 0){
            sala.setRecursos(recursos);
        }
        if(jComboBox1.getSelectedIndex() == 0){
            sala.setEstado(true);
        }
        else{
            sala.setEstado(false);
        }
        sala.setUbicacion(jTextField1.getText());
        JOptionPane.showMessageDialog(this, "La sala ha sido actualizada con éxito");
        AplSystem.guardarXML(AplSystem.getSalas(),"salasDB");
        jTextField1.setText("");
        jComboBox2.setSelectedIndex(0);
        jComboBox1.setSelectedIndex(0);
        jTextArea1.setText("");
        jTextField1.setText("");
        jTextField2.setText("");
        jComboBox2.setEnabled(false);
        jComboBox1.setEnabled(false);
        jButton2.setEnabled(false);
        jButton3.setEnabled(false);
        jTextArea1.setEnabled(false);
    }//GEN-LAST:event_jButton3ActionPerformed

