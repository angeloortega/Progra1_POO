    private void crearReserva(){
        this.setEnabled(false);
        String seleccionada = (String) jComboBox1.getSelectedItem();
        Sala sala = AplSystem.getSala((String)jComboBox1.getSelectedItem());
        Estudiante estudiante = AplSystem.getSesionActiva();
        int anio = dateAndTime.getYear();
        int mes = dateAndTime.getMonthValue();
        int dia = dateAndTime.getDayOfMonth();
        int horaInicio = dateAndTime.toLocalTime().getHour();
        int minInicio = dateAndTime.toLocalTime().getMinute();
        int horaFin = time.getHour();
        int minFin = time.getMinute();
        Reserva reserva;
        reserva = new Reserva(sala,estudiante,anio,mes,dia,horaInicio,minInicio,horaFin,minFin);
        reserva.setCodigo(sala.getId()+reserva.getId()+estudiante.getCarne());
        AplSystem.agregarReserva(reserva);
        sala.agregarReserva();
        estudiante.agregarReserva();
        sendSMS sms=new sendSMS("Su código de calificación de reserva es: " + reserva.getCodigo(),estudiante.getNumero());
        sms.sendSms();
        String emisor="edjiron@gmail.com";
        String contraseña="jqalrtwrjxnkhpic";
        ArrayList<String> receptores=new ArrayList<>();
        for(Participante i:participantes){
            receptores.add(i.getCorreo());
            reserva.addParticipante(i);
        }
        jProgressBar1.setValue(25);
        String asunto=jTextField3.getText();
        String texto="El estudiante "+estudiante.getNombre()+" lo ha invitado para "+asunto+" en la sala "
                +sala.getId()+ " ubicada en "+sala.getUbicacion()+". La reunión empieza "+reserva.getInicio().toString()+
                        " y concluye "+reserva.getFin().toString()+".";
        Correo correo=new Correo(emisor,contraseña,receptores,asunto,texto);
        try {
            jProgressBar1.setValue(50);
            correo.sendCorreo();
        } catch (MessagingException ex) {
            Logger.getLogger(VentanaCrearReserva.class.getName()).log(Level.SEVERE, null, ex);
        }
        jProgressBar1.setValue(100);
        AplSystem.guardarXML(AplSystem.getEstudiantes(), "estudiantesDB");
        AplSystem.guardarXML(AplSystem.getSalas(), "salasDB");
        AplSystem.guardarXML(AplSystem.getReservas(), "reservasDB");
        this.setEnabled(true);
        JOptionPane.showMessageDialog(this, "Operación exitosa");
        jComboBox1.removeAllItems();
        jComboBox1.setEnabled(false);
        jTextField3.setEnabled(false);
        jTextField5.setEnabled(false);
        jTextField6.setEnabled(false);
        jButton3.setEnabled(false);
        jButton4.setEnabled(false);
        jTextArea2.setEnabled(true);
        jTextField3.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextArea2.setText("");
        jTextArea1.setText("");
        participantes = new ArrayList<>();
        contador = 1;
        jProgressBar1.setValue(0);
        jProgressBar1.setVisible(false);
    }
