    /**
     * Checks that the ID introduced exists and displays the information to
     * be changed
     * @param evt 
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = jTextField2.getText();
        id = id.toUpperCase();
        if(AplSystem.salaExiste(id)){
            sala = AplSystem.getSala(id);
            jTextField1.setEnabled(true);
            jComboBox2.setEnabled(true);
            jComboBox1.setEnabled(true);
            jButton2.setEnabled(true);
            jButton3.setEnabled(true);
            jTextArea1.setEnabled(true);
            jTextField1.setText(sala.getUbicacion());
            if(sala.isEstado()){
                jComboBox1.setSelectedIndex(0);
            }
            else{
                jComboBox1.setSelectedIndex(1);
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "El identificador de sala "
                                + id + " no está registrado en el sistema");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

